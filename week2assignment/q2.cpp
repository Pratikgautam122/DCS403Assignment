#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    double num;
    cout << "Angle : ";
    cin >> num;
    cout << "sin(" << num << ") = " << sin(num)<<endl;
    cout << "cos(" << num << ") = " << cos(num)<<endl;
    cout << "tan(" << num << ") = " << tan(num)<<endl;
    return 0;
}